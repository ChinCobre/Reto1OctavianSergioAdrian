	//FUNCION SUMA (Suma n1 con n2)
	public static int suma( int n1, int n2) {
		
		return n1+n2;
	}
	
	
	//FUNCION RESTA (Resta n1 con n2)
	public static int resta( int n1, int n2) {
		
		return n1-n2;
	}
	
	
